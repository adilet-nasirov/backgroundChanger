const changeColor = () => {
  if (colorType === "simple") {
    let randNum = Math.floor(Math.random() * CSS_COLOR_NAMES.length);
    body.style.backgroundColor = CSS_COLOR_NAMES[randNum];
    span.innerHTML = CSS_COLOR_NAMES[randNum];
  } else if (colorType === "rgb") {
    let randomColorSimple = `rgb(${Math.floor(
      Math.random() * 255
    )},${Math.floor(Math.random() * 255)},${Math.floor(Math.random() * 255)})`;
    body.style.backgroundColor = randomColorSimple;
    span.innerHTML = randomColorSimple;
  } else {
    let randomColor = Math.floor(Math.random() * 16777215).toString(16);
    body.style.backgroundColor = "#" + randomColor;
    span.innerHTML = "#" + randomColor;
  }
};

